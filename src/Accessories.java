public class Accessories {
    private String name, type, effect;

    /**เป็น methods ที่สร้าง Accessories
     * @param name  เป็นชื่อของ Accessories
     * @param type  เป็นชนิดของ Accessories
     * @param effect  ผลที่ให้ Accessories
     * effects: ทำการเปลี่ยนค่าใน field ตาม parameter
     * */
    public Accessories(String name, String type, String effect) {
        this.name = name;
        this.type = type;
        this.effect = effect;
    }


}
